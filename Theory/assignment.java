package Theory;
class assignment
{
    public static void main(int a,int d)
    {
        System.out.println(a+=d);           //a=a+d
        System.out.println(a-=d);           //a=a-d
        System.out.println(a*=d);           //a=a*d
        System.out.println(a/=d);           //a=a/d
        System.out.println(a%=d);           //a=a%d
        System.out.println(a&=d);           //a=a&d
        System.out.println(a|=d);           //a=a|d
        System.out.println(a^=d);           //a=a^d
        System.out.println(a<<=d);          //a=a<<d
        System.out.println(a>>=d);          //a=a>>d
        System.out.println(a>>>=d);         //a=a>>>d
    }
}
